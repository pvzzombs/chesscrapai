var gameIsOver = function(){
	if(game.game_over()){
		var fen = game.fen();

		if(game.in_checkmate()){
			if(fen.indexOf("w") > -1){
				gameInfo = gameInfo.replace('[Result "?-?"]', '[Result "0-1"]');
				renderMoveHistory(game.history());
				return {
					winner: "Black",
					loser: "White"
				};
			}else if(fen.indexOf("b") > -1){
				gameInfo = gameInfo.replace('[Result "?-?"]', '[Result "1-0"]');
				renderMoveHistory(game.history());
				return {
					winner: "White",
					loser: "Black"
				};
			}
		}else if(game.in_draw()){
			gameInfo = gameInfo.replace('[Result "?-?"]', '[Result "1/2-1/2"]');
			renderMoveHistory(game.history());
			return {
				winner: "Draw",
				loser: "Draw"
			};
		}
	}
	return {
		winner: "None",
		loser: "None"
	};
};

